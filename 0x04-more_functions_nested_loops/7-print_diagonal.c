#include "main.h"

/**
 * print_diagonal - Entry point
 *
 * Description: Draws a diagonal on the terminal
 * @n: integer
 * Return: A diagonal line
 */
void print_diagonal(int n)
{
	int a, b;

	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		for (a = 0; a < n; a++)
		{
			for (b = 0; b < a; b++)
			{
				_putchar(' ');
			}
			_putchar('\\');
			_putchar('\n');
		}
	}
}
